#include "minirt.h"

t_vec3	v3_normalize(t_vec3 v)
{
	double	d;

	d = hypot(v.x, hypot(v.y, v.z));
	return ((t_vec3){.x = v.x / d, .y = v.y / d, .z = v.z / d});
}
